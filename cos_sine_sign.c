int		sin_sign(float theta)
{
	if (theta >= 0 && theta <= 180)
		return (1);
	return (-1);
}

int		cos_sign(float theta)
{
	if ((theta >= 0 && theta <= 90) || theta >= 270)
		return (1);
	return (-1);
}
